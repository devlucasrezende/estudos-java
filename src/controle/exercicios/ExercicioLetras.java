package controle.exercicios;

public class ExercicioLetras {
	
	public static void main(String[] args) {

		String nome = "Lucas";

		for (int i = 0; i < nome.length(); i++) {

			for (int j = 0; j <= 0; j++) {

				System.out.print(nome.charAt(i));

			}
			
			System.out.println();
		}

	}

}


	

